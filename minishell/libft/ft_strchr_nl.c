#include "libft.h"

char	*ft_strchr_nl(const char *s)
{
	size_t	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == '\n')
			return ((char *)(s + i));
		i++;
	}
	return (NULL);
}
