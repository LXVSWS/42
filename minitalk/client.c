#include "minitalk.h"

int main(int ac, char **av)
{
	if (ac == 3)
	{
		if (*av[2] == '0')
			kill(atoi(av[1]), SIGUSR1);
		else if (*av[2] == '1')
			kill(atoi(av[1]), SIGUSR2);
	}
	else
		write(1, "Error\n", 6);
}
